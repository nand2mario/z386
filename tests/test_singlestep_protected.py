def write_memhex(ram_entries):
    """
    Write sparse hex file for testbench $readmemh.
    ram_entries: list of [addr, value] pairs (byte-level)
    Returns: path to temporary hex file
    """
    f = tempfile.NamedTemporaryFile(mode='w', suffix='.hex', delete=False,
                                    dir=str(TESTS))
    # Group by 4-byte aligned address for readmemh
    for addr, val in sorted(ram_entries, key=lambda x: x[0]):
        f.write(f"@{addr:X} {val:02X}\n")
    f.close()
    return f.name


def parse_seg_result(line):
    """Parse RESULT SEG line into dict.
    Format: RESULT SEG: cs base=0x00000000 limit=0xfffff flags=0xa1e0
    Or:     RESULT SEG: tr sel=0x0028 base=0x00004000 limit=0x00067 flags=0xb120
    Returns: (seg_name, {base, limit, flags, [sel]})
    """
    parts = line.split(':', 1)[1].strip()
    seg_name = parts.split()[0]
    base_m = re.search(r'base=0x([0-9a-fA-F]+)', parts)
    limit_m = re.search(r'limit=0x([0-9a-fA-F]+)', parts)
    flags_m = re.search(r'flags=0x([0-9a-fA-F]+)', parts)
    sel_m = re.search(r'sel=0x([0-9a-fA-F]+)', parts)
    result = {
        'base': int(base_m.group(1), 16) if base_m else 0,
        'limit': int(limit_m.group(1), 16) if limit_m else 0,
        'flags': int(flags_m.group(1), 16) if flags_m else 0,
    }
    if sel_m:
        result['sel'] = int(sel_m.group(1), 16)
    return seg_name, result


def run_test(test, verbose=False, notrace=True):
    """
    Run a single test case through the z386 testbench.

    Args:
        test: Test case dictionary (from JSON)
        verbose: Enable verbose trace output
        notrace: Disable VCD tracing (faster)

    Returns:
        tuple: (passed, errors, output_lines)
    """
    name = test.get('name', 'unknown')
    init = test['initial']
    fin = test['final']

    init_regs = init.get('regs', {})
    init_segs = init.get('segs', {})
    init_dtables = init.get('dtables', {})
    cr0_val = init.get('cr0', 1)  # Default: protected mode

    # Prepare RAM entries (instruction bytes + any initial RAM)
    ram_entries = init.get('ram', [])

    # Place instruction bytes at CS:EIP physical address
    # (prefetch bypasses paging, so we need bytes at the linear address)
    instr_bytes = test.get('bytes', [])
    if instr_bytes:
        cs_base = init_segs.get('cs', {}).get('base', 0)
        eip = init_regs.get('eip', 0)
        code_addr = cs_base + eip
        for i, b in enumerate(instr_bytes):
            ram_entries.append([code_addr + i, b])

    memhex = write_memhex(ram_entries)

    # Build command
    cmd = [str(TB), f"+mem={memhex}", "+cycles=50000"]

    if notrace:
        cmd.append("+notrace")

    if verbose:
        cmd.extend([
            "+trace_decode",
            "+trace_ucode",
            "+trace_sptr",
            "+trace_ldsg",
            "+trace_prot",
            "+trace_descw",
        ])

    # CR0
    cmd.append(f"+cr0={cr0_val}")

    # General registers
    for reg in ['eax', 'ecx', 'edx', 'ebx', 'esp', 'ebp', 'esi', 'edi']:
        val = init_regs.get(reg, 0)
        cmd.append(f"+{reg}={val}")

    # EIP and EFLAGS
    cmd.append(f"+ip={init_regs.get('eip', 0)}")
    cmd.append(f"+eflags={init_regs.get('eflags', 2)}")

    # Segment selectors
    for seg in ['cs', 'ds', 'es', 'ss', 'fs', 'gs']:
        seg_info = init_segs.get(seg, {})
        sel = seg_info.get('sel', 0)
        cmd.append(f"+{seg}={sel}")

    # Segment caches (only when PE=1)
    if cr0_val & 1:
        for seg in ['cs', 'ds', 'es', 'ss', 'fs', 'gs']:
            seg_info = init_segs.get(seg, {})
            cmd.append(f"+{seg}_base={seg_info.get('base', 0)}")
            cmd.append(f"+{seg}_limit={seg_info.get('limit', 0xFFFFF)}")
            cmd.append(f"+{seg}_flags={seg_info.get('flags', 0x21E0)}")

        # GDT/IDT
        gdt = init_dtables.get('gdt', {})
        idt = init_dtables.get('idt', {})
        cmd.append(f"+gdt_base={gdt.get('base', 0)}")
        cmd.append(f"+gdt_limit={gdt.get('limit', 0x3FF)}")
        cmd.append(f"+idt_base={idt.get('base', 0)}")
        cmd.append(f"+idt_limit={idt.get('limit', 0x3FF)}")

    # CR3
    cr3_val = init.get('cr3', 0)
    if cr3_val:
        cmd.append(f"+cr3={cr3_val}")

    # Task register
    tr_info = init.get('tr', {})
    if tr_info.get('sel', 0):
        cmd.append(f"+tr_sel={tr_info['sel']}")
        cmd.append(f"+tr_base={tr_info.get('base', 0)}")
        cmd.append(f"+tr_limit={tr_info.get('limit', 0)}")
        cmd.append(f"+tr_flags={tr_info.get('flags', 0)}")

    # Release segment caches if test requires it
    if init.get('release_segs', False):
        cmd.append("+release_segs")

    # Request RAM outputs we need to check
    final_ram = fin.get('ram', [])
    for i, (addr, _) in enumerate(final_ram):
        cmd.append(f"+ram{i}={addr}")

    if verbose:
        print(f"\nTest: {name}")
        print(f"Instruction bytes: {' '.join(f'{b:02x}' for b in test.get('bytes', []))}")
        print(f"Command: {' '.join(cmd)}")

    # Run testbench
    try:
        p = sp.run(cmd, cwd=str(TESTS), stdout=sp.PIPE, stderr=sp.STDOUT, text=True)
        out = p.stdout

        # Parse results
        result_fault_vector = None
        result_regs = {}
        result_segs = {}
        mem_results = {}

        for line in out.splitlines():
            if line.startswith('RESULT REG:'):
                vals = line.split(':', 1)[1].strip().split()
                for val in vals:
                    if '=' in val:
                        k, v = val.split('=')
                        k = k.lower()
                        if k == 'ip':
                            k = 'eip'
                        if v.startswith('0x'):
                            result_regs[k] = int(v, 16)
                        else:
                            result_regs[k] = int(v)
            elif line.startswith('RESULT SEG:'):
                seg_name, seg_data = parse_seg_result(line)
                result_segs[seg_name] = seg_data
            elif line.startswith('RESULT MEM:'):
                parts = line.split(':', 1)[1].strip().split('=')
                if len(parts) == 2:
                    addr = int(parts[0][1:])  # Remove '@' prefix
                    mem_results[addr] = int(parts[1])
            elif line.startswith('RESULT FAULT:'):
                m = re.search(r'vector=(\d+)', line)
                if m:
                    result_fault_vector = int(m.group(1))

        # Check for TIMEOUT
        if 'TIMEOUT' in out:
            return False, [f"TIMEOUT: test {name}"], out.splitlines()

        # Compare results
        ok = True
        errors = []

        # Fault expectation: if present, just note it for diagnostics.
        # Actual correctness is verified via registers, segments, and RAM
        # (which encode the full post-fault state including stack pushes).
        expected_fault = fin.get('fault')

        # Check registers
        final_regs = fin.get('regs', {})
        for reg, expected in final_regs.items():
            reg = reg.lower()
            got = result_regs.get(reg)

            if got is None:
                errors.append(f"Register {reg} not found in results")
                ok = False
                continue

            # Mask EFLAGS bits that are undefined for certain instructions
            if reg == 'eflags':
                # Mask reserved bits and RF (bit 16)
                mask = 0xFFFEFFFF  # mask out RF
                expected = expected & mask
                got = got & mask

            if got != expected:
                errors.append(f"{reg}: expected 0x{expected:08x}, got 0x{got:08x}")
                ok = False

        # Check segments
        final_segs = fin.get('segs', {})
        for seg, expected_seg in final_segs.items():
            got_seg = result_segs.get(seg)
            if got_seg is None:
                errors.append(f"Segment {seg} not found in results")
                ok = False
                continue

            # Check selector (from result_regs for normal segs, from RESULT SEG for tr/ldt)
            if 'sel' in expected_seg:
                if 'sel' in got_seg:
                    got_sel = got_seg['sel']
                else:
                    got_sel = result_regs.get(seg, 0)
                if got_sel != expected_seg['sel']:
                    errors.append(f"{seg}.sel: expected 0x{expected_seg['sel']:04x}, got 0x{got_sel:04x}")
                    ok = False

            # Check base
            if 'base' in expected_seg and got_seg['base'] != expected_seg['base']:
                errors.append(f"{seg}.base: expected 0x{expected_seg['base']:08x}, got 0x{got_seg['base']:08x}")
                ok = False

            # Check limit (86Box reports byte limit; our testbench reports 20-bit field)
            if 'limit' in expected_seg:
                exp_limit = expected_seg['limit']
                got_limit = got_seg['limit']
                # Convert 86Box byte limit to 20-bit field if needed
                exp_flags = expected_seg.get('flags', 0)
                if exp_limit > 0xFFFFF:
                    if exp_flags & 0x40:  # G bit set
                        exp_limit = (exp_limit >> 12) & 0xFFFFF
                    else:
                        exp_limit = 0xFFFFF  # Null selector: byte limit can't fit in 20-bit
                if got_limit != exp_limit:
                    errors.append(f"{seg}.limit: expected 0x{exp_limit:05x}, got 0x{got_limit:05x}")
                    ok = False

            # Check flags
            if 'flags' in expected_seg and got_seg['flags'] != expected_seg['flags']:
                errors.append(f"{seg}.flags: expected 0x{expected_seg['flags']:04x}, got 0x{got_seg['flags']:04x}")
                ok = False

        # Check RAM
        for addr, expected_val in final_ram:
            got_val = mem_results.get(addr)
            if got_val is None:
                errors.append(f"RAM @{addr:08x} not found in results")
                ok = False
            elif got_val != expected_val:
                errors.append(f"RAM @{addr:08x}: expected 0x{expected_val:02x}, got 0x{got_val:02x}")
                ok = False

        return ok, errors, out.splitlines()
    finally:
        os.unlink(memhex)


def run_test_wrapper(args):
    """Wrapper for multiprocessing Pool."""
    test, = args
    return run_test(test, verbose=False, notrace=True)


def extract_category(test):
    """Extract category from test dict. Uses 'category' JSON field if present,
    otherwise falls back to heuristic parsing of the test name."""
    if isinstance(test, dict) and 'category' in test:
        return test['category']
    # Fallback for old test files without category field
    name = test.get('name', 'unknown') if isinstance(test, dict) else str(test)
    struct = name.split(' ', 1)[0] if ' ' in name else name
    parts = struct.split('_')
    if parts and parts[-1].isdigit():
        parts = parts[:-1]
    return parts[-1] if parts else 'unknown'


